package CodeWithHery.com.io;
public class CWH_027_02_ForEach_Loop_ON_Array {
    public static void main(String[] args){
        //declaring an array
        int[] arr ={1,2,3,3,4,5};
        //traversing the array with for-each loop
        for(int i:arr){
            System.out.println(i);
        }
    }
}
